THIS_DIR = Path(__file__).resolve().parents[1]

# Real measured latencies (ms) on H200, fp16 / bfloat16
QWEN_LATENCY_MS = 324                  # measured greedy on Qwen2.5-7B
DEFAULT_TEACHER_LATENCY_MS = 4562      # ID median; we override per OOD using actual

PROBE_NAMES = ["DCP_mlp", "SEPs_logreg", "SEPs_ridge"]
PROBE_PRETTY = {
    "DCP_mlp": "DCP-MLP (ours)",
    "SEPs_logreg": "SEPs-LR",
    "SEPs_ridge": "SEPs-Ridge (Kossen 2024)",
}
PROBE_COLORS = {
    "DCP_mlp": "#d62728",
    "SEPs_logreg": "#1f77b4",
    "SEPs_ridge": "#7f7f7f",
}
PROBE_MARKERS = {
    "DCP_mlp": "o", "SEPs_logreg": "s", "SEPs_ridge": "D",
}
DATASET_PRETTY = {
    "id": "TriviaQA (ID)",
    "hotpotqa": "HotpotQA dev_distractor (OOD)",
    "nq": "NQ-Open validation (OOD)",
}


# ---------------------------------------------------------------------------
# Build aligned per-question table
# ---------------------------------------------------------------------------


def build_aligned_table(dataset: str) -> tuple[pd.DataFrame, dict]:
    """Return (table, meta) for the given OOD dataset name in {hotpotqa, nq}."""
    # 1) Qwen-7B sample0 strict_correct from OOD npz
    npz = np.load(THIS_DIR / "runs" / "qwen7b" / f"{dataset}_ood.npz",
                  allow_pickle=True)
    qids = list(npz["question_ids"])
    q_correct = list(npz["strict_correct"])
    qwen_per_q = pd.DataFrame({
        "question_id": qids,
        "qwen_sample0_correct": [float(c) for c in q_correct],
    })

    # 2) Teacher correctness + latency from JSONL
    t_path = THIS_DIR / "runs" / f"teacher_oss120b_{dataset}.jsonl"
    teacher_records = []
    with open(t_path) as f:
        for line in f:
            r = json.loads(line)
            if r.get("missing"):
                continue
            teacher_records.append({
                "question_id": r["question_id"],
                "teacher_greedy_correct": float(r.get("teacher_strict_correct", 0)),
                "teacher_latency_ms": float(r.get("latency_ms", 0)),
                "teacher_completion_tokens": int(r.get("completion_tokens", 0)),
            })
    teacher_per_q = pd.DataFrame(teacher_records)

    # 3) Probe scores from per_item_predictions_all.csv
    pred = pd.read_csv(THIS_DIR / "results" / "per_item_predictions_all.csv")
    pred = pred[(pred["base"] == "qwen7b") & (pred["dataset"] == dataset)]

    table = qwen_per_q.merge(teacher_per_q, on="question_id", how="inner")
    for probe in PROBE_NAMES:
        psub = pred[pred["probe"] == probe][["question_id", "y_score"]]
        psub = psub.rename(columns={"y_score": f"score_{probe}"})
        table = table.merge(psub, on="question_id", how="left")

    n_dropped = len(qwen_per_q) + len(teacher_per_q) - 2 * len(table)
    teacher_lat_med = (table["teacher_latency_ms"].median()
                       if "teacher_latency_ms" in table else DEFAULT_TEACHER_LATENCY_MS)
    meta = {
        "dataset": dataset,
        "n_aligned": len(table),
        "n_dropped_misalign": n_dropped,
        "qwen_acc": float(table["qwen_sample0_correct"].mean()),
        "teacher_acc": float(table["teacher_greedy_correct"].mean()),
        "teacher_latency_ms_median": float(teacher_lat_med),
        "teacher_latency_ms_p90": float(table["teacher_latency_ms"].quantile(0.9)),
        "teacher_completion_tokens_median": float(table["teacher_completion_tokens"].median()),
        "cost_ratio_teacher_over_qwen": float(teacher_lat_med / QWEN_LATENCY_MS),
    }
    return table, meta


# ---------------------------------------------------------------------------
# Cascade simulation (percentile-based escalation)
# ---------------------------------------------------------------------------


def simulate_cascade(table: pd.DataFrame, probe: str, escalation_rate: float,
                     cost_teacher: float) -> tuple[float, float, float, float]:
    score = table[f"score_{probe}"].to_numpy()
    qwen_correct = table["qwen_sample0_correct"].to_numpy()
    teacher_correct = table["teacher_greedy_correct"].to_numpy()
    if escalation_rate <= 0:
        threshold = float(np.min(score)) - 1.0
        commit_qwen = np.ones(len(score), dtype=bool)
    elif escalation_rate >= 1:
        threshold = float(np.max(score)) + 1.0
        commit_qwen = np.zeros(len(score), dtype=bool)
    else:
        threshold = float(np.quantile(score, escalation_rate))
        commit_qwen = score > threshold
    correct = np.where(commit_qwen, qwen_correct, teacher_correct)
    cost = np.where(commit_qwen, 1.0, 1.0 + cost_teacher)
    actual_frac = float((~commit_qwen).mean())
    return float(cost.mean()), float(correct.mean()), actual_frac, threshold


def simulate_random(table: pd.DataFrame, p_keep: float, cost_teacher: float,
                    n_trials: int = 50, seed: int = 0) -> tuple[float, float]:
    rng = np.random.default_rng(seed)
    qc = table["qwen_sample0_correct"].to_numpy()
    tc = table["teacher_greedy_correct"].to_numpy()
    costs, accs = [], []
    for _ in range(n_trials):
        commit_qwen = rng.random(len(table)) < p_keep
        correct = np.where(commit_qwen, qc, tc)
        cost = np.where(commit_qwen, 1.0, 1.0 + cost_teacher)
        costs.append(cost.mean()); accs.append(correct.mean())
    return float(np.mean(costs)), float(np.mean(accs))


def simulate_oracle(table: pd.DataFrame, cost_teacher: float) -> tuple[float, float, float]:
    qc = table["qwen_sample0_correct"].to_numpy()
    tc = table["teacher_greedy_correct"].to_numpy()
    commit_qwen = qc > 0.5
    correct = np.where(commit_qwen, qc, tc)
    cost = np.where(commit_qwen, 1.0, 1.0 + cost_teacher)
    return float(cost.mean()), float(correct.mean()), float((~commit_qwen).mean())


def sweep_all_strategies(table: pd.DataFrame, cost_teacher: float) -> pd.DataFrame:
    rows = []
    qa = float(table["qwen_sample0_correct"].mean())
    ta = float(table["teacher_greedy_correct"].mean())
    rows.append({"strategy": "always_qwen", "probe": None, "threshold": np.nan,
                 "cost": 1.0, "accuracy": qa, "frac_escalated": 0.0})
    rows.append({"strategy": "always_teacher", "probe": None, "threshold": np.nan,
                 "cost": cost_teacher, "accuracy": ta, "frac_escalated": 1.0})
    rates = np.linspace(0.0, 1.0, 101)
    for probe in PROBE_NAMES:
        for r in rates:
            cost, acc, actual_frac, th = simulate_cascade(table, probe, r, cost_teacher)
            rows.append({"strategy": "probe_cascade", "probe": probe,
                         "threshold": th, "cost": cost, "accuracy": acc,
                         "frac_escalated": actual_frac, "target_escalation_rate": r})
    for p in np.linspace(0.0, 1.0, 41):
        cost, acc = simulate_random(table, p, cost_teacher)
        rows.append({"strategy": "random_cascade", "probe": None,
                     "threshold": p, "cost": cost, "accuracy": acc,
                     "frac_escalated": 1.0 - p})
    cost, acc, fe = simulate_oracle(table, cost_teacher)
    rows.append({"strategy": "oracle_cascade", "probe": None, "threshold": np.nan,
                 "cost": cost, "accuracy": acc, "frac_escalated": fe})
    return pd.DataFrame(rows)


def cost_savings_at_acc(df: pd.DataFrame, targets: list[float],
                        cost_teacher: float) -> pd.DataFrame:
    rows = []
    for tgt in targets:
        always_q = df[df["strategy"] == "always_qwen"].iloc[0]
        always_t = df[df["strategy"] == "always_teacher"].iloc[0]
        for probe in PROBE_NAMES:
            sub = df[(df["strategy"] == "probe_cascade") & (df["probe"] == probe)]
            meet = sub[sub["accuracy"] >= tgt].sort_values("cost")
            if meet.empty:
                cheapest = "—"
                savings = "—"
            else:
                r = meet.iloc[0]
                cheapest = f"{r['cost']:.2f} (esc={r['frac_escalated']:.0%})"
                savings = f"{(always_t['cost'] - r['cost']) / always_t['cost'] * 100:+.0f}%"
            rows.append({"target_acc": tgt,
                         "probe": PROBE_PRETTY[probe],
                         "cheapest_cost": cheapest,
                         "savings_vs_always_teacher": savings,
                         "always_qwen_meets": "✓" if always_q["accuracy"] >= tgt else "✗"})
    return pd.DataFrame(rows)


