"""2-tier multi-agent cascade analysis on OOD distributions.

Mirrors `make_cascade_2tier.py` (TriviaQA ID) but uses:
  - Qwen-7B sample0 strict_correct from `runs/qwen7b/<dataset>_ood.npz`
  - GPT-OSS-120B greedy strict_correct + per-question latency from
    `runs/teacher_oss120b_<dataset>.jsonl` (generated by run_teacher_on_ood.py)
  - Probe scores (DCP-MLP, SEPs-LR, SEPs-Ridge) from
    `results/per_item_predictions_all.csv` filtered by
    base="qwen7b", dataset in {"hotpotqa","nq"}.

Cost units: Qwen-7B K=1 = 1.0; teacher cost = actual median GPT-OSS latency
(measured per OOD dataset) divided by Qwen-7B latency.

Outputs (per dataset):
  results/cascade_2tier_<dataset>_results.csv
  results/cascade_2tier_<dataset>_savings.csv
  reports/dashboard_cascade_2tier_<dataset>.png
And a combined report:
  results/cascade_2tier_ood_summary.csv
  reports/dashboard_cascade_2tier_ood.png   (3-row layout: ID + 2 OOD)
"""
from __future__ import annotations

