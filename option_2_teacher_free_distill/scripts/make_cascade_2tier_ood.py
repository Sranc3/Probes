def panel_pareto(ax, df: pd.DataFrame, table: pd.DataFrame, meta: dict) -> None:
    aq = df[df["strategy"] == "always_qwen"].iloc[0]
    at = df[df["strategy"] == "always_teacher"].iloc[0]
    ax.scatter(aq["cost"], aq["accuracy"], s=160, marker="s", color="#3182bd",
               edgecolors="black", linewidths=0.7, zorder=4)
    ax.annotate("Always Qwen-7B", (aq["cost"], aq["accuracy"]),
                textcoords="offset points", xytext=(8, -16), fontsize=8.5,
                color="#3182bd")
    ax.scatter(at["cost"], at["accuracy"], s=180, marker="*", color="#000000",
               edgecolors="black", linewidths=0.7, zorder=4)
    ax.annotate("Always GPT-OSS-120B", (at["cost"], at["accuracy"]),
                textcoords="offset points", xytext=(-110, -3), fontsize=8.5)

    rc = df[df["strategy"] == "random_cascade"].sort_values("cost")
    ax.plot(rc["cost"], rc["accuracy"], color="#bcbcbc", linewidth=1.0,
            marker="x", markersize=3.5, alpha=0.6,
            label="Random cascade", zorder=2)
    for probe in PROBE_NAMES:
        sub = df[(df["strategy"] == "probe_cascade") & (df["probe"] == probe)]
        sub = sub.sort_values("cost")
        ax.plot(sub["cost"], sub["accuracy"],
                color=PROBE_COLORS[probe], marker=PROBE_MARKERS[probe],
                markersize=3.5, linewidth=1.6, alpha=0.95,
                label=f"{PROBE_PRETTY[probe]}", zorder=5)
    oc = df[df["strategy"] == "oracle_cascade"]
    if not oc.empty:
        r = oc.iloc[0]
        ax.scatter(r["cost"], r["accuracy"], s=180, marker="v",
                   color="#2ca02c", edgecolors="black", linewidths=0.8, zorder=6)
        ax.annotate(f"Oracle\n(cost={r['cost']:.2f}, acc={r['accuracy']:.2f})",
                    (r["cost"], r["accuracy"]),
                    textcoords="offset points", xytext=(-10, -36), fontsize=8,
                    color="#2ca02c")
    ax.set_xlabel("Avg cost / question (Qwen-7B forward units)")
    ax.set_ylabel("Avg accuracy")
    ax.set_xscale("log")
    ax.set_title(
        f"{DATASET_PRETTY[meta['dataset']]} — n={meta['n_aligned']} | "
        f"Qwen={meta['qwen_acc']:.3f}  GPT-OSS={meta['teacher_acc']:.3f}  "
        f"cost ratio={meta['cost_ratio_teacher_over_qwen']:.1f}x",
        fontsize=10,
    )
    ax.grid(linestyle=":", alpha=0.4)
    ax.legend(loc="lower right", fontsize=8.0, framealpha=0.95)
    ymin = max(0.0, min(aq["accuracy"], at["accuracy"]) - 0.05)
    ymax = min(1.0, max(at["accuracy"], aq["accuracy"]) + 0.10)
    ax.set_ylim(ymin, ymax)


def panel_acc_vs_escalation(ax, df: pd.DataFrame, table: pd.DataFrame, meta: dict) -> None:
    for probe in PROBE_NAMES:
        sub = df[(df["strategy"] == "probe_cascade") & (df["probe"] == probe)]
        sub = sub.sort_values("frac_escalated")
        ax.plot(sub["frac_escalated"] * 100, sub["accuracy"],
                color=PROBE_COLORS[probe], marker=PROBE_MARKERS[probe],
                markersize=2.8, linewidth=1.6,
                label=f"{PROBE_PRETTY[probe]}")
    aq = float(table["qwen_sample0_correct"].mean())
    at = float(table["teacher_greedy_correct"].mean())
    ax.axhline(aq, color="#3182bd", linestyle="--", linewidth=0.8, alpha=0.5)
    ax.axhline(at, color="#000000", linestyle="--", linewidth=0.8, alpha=0.5)
    ax.text(2, aq + 0.005, f"Qwen alone: {aq:.3f}", fontsize=8, color="#3182bd")
    ax.text(2, at - 0.012, f"Teacher alone: {at:.3f}", fontsize=8)
    ax.set_xlabel("% escalated to teacher")
    ax.set_ylabel("Avg accuracy")
    ax.set_title(f"Accuracy vs escalation budget", fontsize=9.5)
    ax.set_xlim(0, 100)
    ax.set_ylim(min(aq, at) - 0.02, max(aq, at) + 0.04)
    ax.grid(linestyle=":", alpha=0.4)
    ax.legend(fontsize=8.0, loc="lower right")


def panel_routing_at_threshold(ax, table: pd.DataFrame, meta: dict,
                               probe: str = "DCP_mlp",
                               escalation_rate: float = 0.30) -> None:
    """Quadrant chart at a fixed escalation rate (percentile-based)."""
    score = table[f"score_{probe}"].to_numpy()
    threshold = float(np.quantile(score, escalation_rate))
    qc = table["qwen_sample0_correct"].to_numpy()
    confident = score > threshold
    correct = qc > 0.5
    quads = {
        "TT": int((confident & correct).sum()),
        "TF": int((confident & (~correct)).sum()),
        "FT": int(((~confident) & correct).sum()),
        "FF": int(((~confident) & (~correct)).sum()),
    }
    n = len(table)
    qwen_acc_when_committed = quads["TT"] / max(quads["TT"] + quads["TF"], 1)
    n_esc = (~confident).sum()
    teacher_corr_when_esc = ((~confident) & (table["teacher_greedy_correct"] > 0.5)).sum()
    teacher_acc_on_esc = teacher_corr_when_esc / max(n_esc, 1)

    cats = ["Conf+Corr\n(commit ✓)", "Conf+WRONG\n(commit ✗)",
            "Unsure+Corr\n(escalate, waste)", "Unsure+Wrong\n(escalate)"]
    counts = [quads["TT"], quads["TF"], quads["FT"], quads["FF"]]
    colors = ["#2ca02c", "#d62728", "#ff7f0e", "#7f7f7f"]
    ax.bar(range(4), counts, color=colors, alpha=0.85,
           edgecolor="black", linewidth=0.5)
    for i, c in enumerate(counts):
        ax.text(i, c + max(counts) * 0.02, f"{c}\n({c/n*100:.0f}%)",
                ha="center", fontsize=7.5)
    ax.set_xticks(range(4))
    ax.set_xticklabels(cats, fontsize=7.5)
    ax.set_ylabel("Question count")
    ax.set_title(f"Routing @ DCP escalation = {escalation_rate:.0%} (th={threshold:.3g})\n"
                 f"Qwen acc when commit: {qwen_acc_when_committed:.2%}  |  "
                 f"Teacher acc on escalated: {teacher_acc_on_esc:.2%}",
                 fontsize=9)
    ax.set_ylim(0, max(counts) * 1.30)
    ax.grid(axis="y", linestyle=":", alpha=0.4)


